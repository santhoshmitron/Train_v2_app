public class Status {
	

	private Boolean is_report_changed;
	private Boolean is_status_changed;
	private Boolean is_closed_pn;

	private Boolean is_open_pn;

	public Boolean getIs_report_changed() {
		return is_report_changed;
	}
	public void setIs_report_changed(Boolean is_report_changed) {
		this.is_report_changed = is_report_changed;
	}
	public Boolean getIs_status_changed() {
		return is_status_changed;
	}
	public void setIs_status_changed(Boolean is_status_changed) {
		this.is_status_changed = is_status_changed;
	}
	public Boolean getIs_closed_pn() {
		return is_closed_pn;
	}
	public void setIs_closed_pn(Boolean is_closed_pn) {
		this.is_closed_pn = is_closed_pn;
	}
	public Boolean getIs_open_pn() {
		return is_open_pn;
	}
	public void setIs_open_pn(Boolean is_open_pn) {
		this.is_open_pn = is_open_pn;
	}

}
